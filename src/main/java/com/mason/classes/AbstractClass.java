package com.mason.classes;

/**
 * <p>Abstract classes possesses all properties of a regular class along with
 * one special privilege to have methods as abstract.
 * Abtract methods are the ones which are declared with an abstract keyword
 * and does not have any method definition i.e. only declaration.
 *
 * <em>Abstract methods must be implemented only by bottom most non-abstract
 * class in the hierarchy.</em>
 *
 * @author monilshah
 * @created 12/12/16
 * @version 1.0
 */
public abstract class AbstractClass {

    public abstract void doAbstraction();

}


